package com.company;

public class A6_operators {
    public static void main(String[] args) {

//  1.    arithmatic op
//    +,-,*,/,%,++,--
//  2.    assignment op
//     =,+=
        int b = 9;
        b += 3;
        b *= 3;
        b -= 3;

        System.out.println(b);
//  3.    comparison op
//    ==,=<x=>
        System.out.println(64>6);
//  4.    logical op
//        && , || , !
        System.out.println(64>6 && 65>9);
        System.out.println(64>6 && 65>94);
        System.out.println(64>6 || 65>94);
        System.out.println(64>69 || 65>94);

//  5.    bitwise op


    }
}
