package com.company;

public class A17_Do_while_loop {
    public static void main(String[] args) {
        int b = 10;
        do {
            System.out.println(b);
            b++;
        }while(b<5);

//        Write a program to print first n natural numbers using a do-while loop.

        int a = 0;
        do {
            System.out.println(a);
            a++;
        } while(a<10000);
    }
}
